<h3>De business rules van  ‘Project-Sync’</h3>
<p>
SoftROC hanteert de volgende business rules voor het declareren van projectkosten:
</br>
</br>
Reiskosten:
</br>
OV-reiskosten worden 100% vergoed.
</br>
</br>
Reiskosten eigen auto worden over de 10 kilometers vergoed a 50 eurocent per kilometer. 
Reizen onder de 10 kilometers mag je niet declareren.
Reizen over de 100 kilometers mag je niet declareren. Hiervoor heb je speciale toestemming nodig.  
</br>
</br>
Boeken:
</br>
Boeken worden t/m 100 euro’s vergoed.
</br>
</br>
Laptops:
</br>
Laptops t/m 1000 euro’s worden vergoed.
</p>